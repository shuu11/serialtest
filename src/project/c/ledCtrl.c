//----------------------------------------------------------------------
//  static variable
//----------------------------------------------------------------------
static BYTE ledSts;

//----------------------------------------------------------------------
//  function table
//----------------------------------------------------------------------
static void fpFunc__(void);
static void fpFunc01(void);

static FUNCPTR fpTable[eLED_STS_MAX] = {
		// イベント
		fpFunc01, //	デフォルト
		fpFunc__, //	エラー
};


//----------------------------------------------------------------------
//  function
//----------------------------------------------------------------------
void ledCtrl(void)
{
	fpTable[ledSts]();
}

static void fpFunc__(void)
{
	__nop();
}

static void fpFunc01(void)
{
	mPO_LED1 = mLED_ON;
	mPO_LED2 = mLED_ON;
}

void ledCtrl_init(void)
{
	mPO_LED1 = mLED_ON;
	mPO_LED2 = mLED_ON;
}

